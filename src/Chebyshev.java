public class Chebyshev {
     //for Chebyshev Distances,
    private final double hrVerticalDistance = 1.0;
    private final double dglDistance = 1.0;

    public Chebyshev() {
    }

    /**
     * @return the hrVerticalDistance
     */
    public double getHrVerticalDistance() {
        return hrVerticalDistance;
    }

   
    /**
     * @return the dglDistance
     */
    public double getDglDistance() {
        return dglDistance;
    }

    
    
    
}
